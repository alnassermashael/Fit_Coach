package com.example.FitCoach;

public class User {
    public String UID;
    public String username;
    public String email;
    public String gender;


    public User(){

    }

    public User(String UID, String username, String email, String gender){

        this.UID = UID;
        this.username = username;
        this.email = email;
        this.gender = gender;


    }

}
